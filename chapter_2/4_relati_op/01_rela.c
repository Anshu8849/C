/*
Relational operators is used to perform relational(comparision) based task on data
These are binary data.
These operators can not modify the operands.
Results of these operators will be 0 or 1.
*/


#include<stdio.h>
int main()
{
	int i=10,j=20;

printf("%d\n",i>j);
printf("%d\n",i>=j);
printf("%d\n",i<j);
printf("%d\n",i<=j);
printf("%d\n",i==j);
printf("%d\n",i!=j);

}








